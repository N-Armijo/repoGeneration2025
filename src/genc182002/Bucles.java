package genc182002;

public class Bucles {
    public static void main(String[] args) {
    //Bucle While
    //Contador de despegue
        int contador = 10;
        System.out.println("Contador de despegue");

        while (contador > 0) {
            contador--;
            System.out.println(contador);
        }
        System.out.println("Despegue");
    }

}
